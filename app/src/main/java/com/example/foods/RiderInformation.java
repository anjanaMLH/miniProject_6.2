package com.example.foods;

public class RiderInformation {

    private String rider_name;
    private String rider_id;
    private String rider_contact_number;

    public RiderInformation() {

    }

    public String getRider_name() {
        return rider_name;
    }

    public void setRider_name(String rider_name) {
        this.rider_name = rider_name;
    }

    public String getRider_id() {
        return rider_id;
    }

    public void setRider_id(String rider_id) {
        this.rider_id = rider_id;
    }

    public String getRider_contact_number() {
        return rider_contact_number;
    }

    public void setRider_contact_number(String rider_contact_number) {
        this.rider_contact_number = rider_contact_number;
    }
}
